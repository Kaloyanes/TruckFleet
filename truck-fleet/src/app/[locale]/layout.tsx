import { Toaster } from "@/components/ui/toaster";
import MotionConfigProvider from "@/context/motion-config-provider";
import { ThemeProvider } from "@/context/theme-provider";
import { locales } from "@/lib/i18n";
import type { Metadata } from "next";
import { NextIntlClientProvider } from "next-intl";
import { getMessages, setRequestLocale } from "next-intl/server";
import { Manrope, Plus_Jakarta_Sans } from "next/font/google";
import "../globals.css";

const poppins = Plus_Jakarta_Sans({
	preload: true,
	weight: ["400", "500", "600", "700", "800"],
	subsets: ["cyrillic-ext", "latin"],
});

const noto = Manrope({
	preload: true,
	weight: ["400", "500", "600", "700", "800"],
	subsets: ["cyrillic", "latin"],
});

export function generateStaticParams() {
	return locales.map((locale) => ({ locale }));
}

export const metadata: Metadata = {
	title: {
		template: "%s | Truck Fleet",
		default: "Truck Fleet",
	},

	description: "Generated by create next app",
	icons: {
		icon: [
			{
				media: "(prefers-color-scheme: light)",
				url: "/packages_light.png",
				href: "/packages_light.png",
			},
			{
				media: "(prefers-color-scheme: dark)",
				url: "/packages_dark.png",
				href: "/packages_dark.png",
			},
		],
	},
};

export default async function RootLayout({
	children,
	params,
}: Readonly<{
	children: React.ReactNode;
	params: { locale: string };
}>) {
	setRequestLocale(params.locale);

	const messages = await getMessages();

	return (
		<html lang={params.locale} suppressHydrationWarning>
			<head>
				<meta charSet="utf-8" />
				<meta name="viewport" content="width=device-width, initial-scale=1" />
			</head>
			<body
				className={noto.className}
				// className={params.locale === "en" ? poppins.className : noto.className}
			>
				<MotionConfigProvider
					props={{
						reducedMotion: "user",
						transition: {
							bounce: 0.2,
							type: "spring",
						},
					}}
				>
					<ThemeProvider
						attribute="class"
						defaultTheme="system"
						enableSystem
						disableTransitionOnChange
					>
						<NextIntlClientProvider messages={messages}>
							<main>{children}</main>
							<Toaster />
						</NextIntlClientProvider>
					</ThemeProvider>
				</MotionConfigProvider>
			</body>
		</html>
	);
}
